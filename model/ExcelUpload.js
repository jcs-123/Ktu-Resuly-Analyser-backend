const mongoose = require('mongoose');

const excelUploadSchema = new mongoose.Schema({
  filename: {
    type: String,
    required: true,
  },
  originalName: {
    type: String,
    required: true,
  },
  path: {
    type: String,
    required: true,
  },
  mimetype: {
    type: String,
    required: true,
  },
  size: {
    type: Number,
    required: true,
  },
}, { timestamps: true });

module.exports = mongoose.model('ExcelUpload', excelUploadSchema);
